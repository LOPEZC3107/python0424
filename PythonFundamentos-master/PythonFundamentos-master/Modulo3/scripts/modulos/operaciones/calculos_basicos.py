

def calcular_suma(x:float,y:float)->float:
    """Retorna la sumatoria de x + y"""
    return x+ y


def calcular_resta(x:float,y:float)->float:
    """Retorna la resta de x - y"""
    return x- y

def calcular_multiplicacion(x:float,y:float)->float:
    """Retorna la multiplicacion de x * y"""
    return x* y